function cek1() {
	var a = document.getElementById("dikt1");

	if(a.value=="4"){
		document.getElementById("dikt1").style="background-color: #CBDBF6";

	} else {
		document.getElementById("dikt1").style="background-color: #FF9F98";

	}
}

